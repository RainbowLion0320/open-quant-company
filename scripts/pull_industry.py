def main():
    # 从 JSON 加载（已通过 Tushare API 拉取并缓存）
    print("加载行业数据...")
    json_path = os.path.join(os.path.dirname(__file__), "..", "data", "tushare_ind.json")
    with open(json_path) as f:
        records = json.load(f)
    df = pd.DataFrame(records)
    print(f"  {len(df)} 只")


    # 建立映射
    symbol_to_sw2 = dict(zip(df['symbol'], df['industry']))
    symbol_to_name = dict(zip(df['symbol'], df['name']))

    # 映射到申万一级
    symbol_to_sw1 = {}
    unmapped = set()
    for sym, sw2 in symbol_to_sw2.items():
        sw1 = SW2_TO_SW1.get(sw2, None)
        if sw1:
            symbol_to_sw1[sym] = sw1
        else:
            unmapped.add(sw2)

    print(f"\n申万一级映射: {len(symbol_to_sw1)} 只")
    print(f"未映射行业: {sorted(unmapped)}")

    # 测试 Top500 覆盖
    from data.symbols import CIRCLE_STOCKS
    hit = sum(1 for s in CIRCLE_STOCKS if s in symbol_to_sw1)
    miss = [s for s in CIRCLE_STOCKS if s not in symbol_to_sw1]
    print(f"\nTop500 覆盖: {hit}/{len(CIRCLE_STOCKS)} 只有行业")
    if miss:
        print(f"缺失: {miss[:10]}...")

    # 保存 JSON
    output = os.path.join(os.path.dirname(__file__), "..", "data", "tushare_industry.json")
    mapping = {
        "sw2": symbol_to_sw2,
        "sw1": symbol_to_sw1,
        "name": symbol_to_name,
        "unmapped_sw2": list(unmapped),
    }
    with open(output, "w") as f:
        json.dump(mapping, f, ensure_ascii=False)
    print(f"\n已保存到 {output}")


if __name__ == "__main__":
    main()
